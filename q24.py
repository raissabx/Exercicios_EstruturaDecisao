n1 = float(input('Digite um número: '))
n2 = float(input('Digite outro número: '))
op = input('Escolha a operação desejada (A-adição, S-subtração, D-divisão, M-multiplicação: ').upper()


if op == 'A':
    resultado = n1 + n2

elif op == 'S':
    resultado = n1 - n2

elif op == 'D':
    resultado = n1 / n2

elif op == 'M':
    resultado = n1 * n2


if (resultado % 2 ) == 0:
    print(f'O número {resultado} é par')

else:
     print(f'O número {resultado} é ímpar')

if resultado < 0:
    print(f'O número {resultado} é negativo')

else:
    print(f'O número {resultado} é positivo')


if (resultado % 1) == 0:
    print(f'O número {resultado} é inteiro')

else: 
    print(f'O número {resultado} é decimal')


